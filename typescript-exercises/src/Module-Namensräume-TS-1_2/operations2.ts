let add = (a: number, b: number) => {
  return a + b;
};

let substract = (a: number, b: number) => {
  return a - b;
};

let divide = (a: number, b: number) => {
  return a / b;
};

let multiply = (a: number, b: number) => {
  return a * b;
};

export {
  add as addNumbers,
  substract as subtractNumbers,
  divide as divideNumbers,
  multiply as multiplyNumbers,
};
